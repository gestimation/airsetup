test_that("create_agentic_project creates required folders and files", {
  path <- tempfile("airsetup-")
  create_agentic_project(path)
  expect_true(dir.exists(file.path(path, "01_study_documentation_package/source")))
  expect_true(file.exists(file.path(path, "AGENTS.md")))
  expect_true(file.exists(file.path(path, "05_qc_package/template/checkpoint_definition_template.csv")))
  expect_true(file.exists(file.path(path, "03_statistical_analysis_package/template/software_inventory_template.csv")))
})

test_that("overwrite behavior preserves existing files by default", {
  path <- tempfile("airsetup-")
  create_agentic_project(path)
  agents <- file.path(path, "AGENTS.md")
  writeLines("custom", agents)
  create_agentic_project(path, overwrite = FALSE)
  expect_equal(readLines(agents), "custom")
  create_agentic_project(path, overwrite = TRUE)
  expect_true(any(grepl("Checkpoint QC", readLines(agents))))
})

